7.BUCLE PARA

7.1.	Imprimir números del 1 al 50 que sean múltiplos de 3 y 5.
7.2.	Contar cuántos números del 1 al 30 son divisibles por 2 o 3.
7.3.	Sumar números del 1 al 50, excluyendo aquellos divisibles por 7.
7.4.	Imprimir la suma entre 1 y X cantidad de números.
7.5.	Escribe un programa que pida ingresar 10 letras y que imprima las vocales cuando son ingresadas.
7.6.	Escribe un programa que pida ingresar 5 números y que sume sólo los pares.
7.7.	Sumar los números impares hasta X y mostrar el resultado.
7.8.	Contar los impares entre 1 y X cantidad de números y mostrar los resultados.
7.9.	Calcular el factorial de N y mostrar el resultado.
7.10.	Mostrar los números primos en un rango de números determinado por el usuario.
7.11.	Contar los números pares en un rango de números determinado por el usuario.
7.12.	Encontrar y mostrar los primeros 15 números primos de 100.
7.13.	Escribe un programa que pida ingresar 10 números y que sume sólo los pares. Si son impares debe restarlos. Mostrar los resultados, indicando cuantos pares e impares se ingresaron.
7.14.	Escribir un programa que pida ingresar una cantidad de letras determinadas por el usuario. Contar cada vocal y mostrar al final.
7.15.	Escribe un programa que pida un número y luego imprima todos los números impares desde 1 hasta ese número.
7.16.	Escribe un programa que sume 5 listados de números y muestre el resultado de cada uno. También debe mostrar el promedio de todos.
7.17.	Escribe un programa que sume 5 listados de números, cada listado debe tener mínimamente 1 número y debe acumularse e indicar cuanto va acumulado. En cada listado se debe preguntar si desea seguir ingresando números o indicar finalizar ingresando “salir” o “s” (considerar alternativas de mayúsculas y minúsculas).
7.18.	Escribe un programa que imprima la tabla de multiplicar del 1 al 10. Debe indicarse cada tabla.
7.19.	Escribe un programa que arme 3 listas de 3 palabras. Debe indicarse el número de cada lista.
7.20.	Escribe un programa que imprima un triángulo rectángulo de asteriscos de 5 filas.
